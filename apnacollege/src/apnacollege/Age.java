package apnacollege;

public class Age {
	public static void main(String[] args) {
		int age=18;
		switch (age) {
		case 16:
			System.out.println("Your Age is 16 .");
			break;
		case 18:
			System.out.println("Your Age is 18 .");
			break;
		case 19:
			System.out.println("Your Age is 19 .");
			break;
		case 20:
			System.out.println("Your Age is 20 .");
			break;
		default:
			System.out.println("Wrong Number");
		}
	}
}
